package com.stulsoft.pjava.basics.converts;

/**
 * Converting Hexadecimal to long
 */
public class HexToLong {
    public static void main(String[] args) {
        System.out.println("==>main");
        long l1 = 0xFFFFFFFFL;
        System.out.println(l1);
        long l2 = 0xFFFFFFFF;
        System.out.println(l2);
        System.out.println("<==main");
    }
}
